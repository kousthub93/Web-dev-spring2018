defmodule Tracker3Web.UserController do
  use Tracker3Web, :controller

  alias Tracker3.Users
  alias Tracker3.Users.User
  alias Tracker3.Repo

  action_fallback Tracker3Web.FallbackController

  def index(conn, _params) do
    users = Users.list_users()
    render(conn, "index.json", users: users)
  end

  def create(conn, %{"user" => user_params}) do

    encrPwd = Comeonin.Argon2.hashpwsalt(user_params["password"])

    with {:ok, %User{} = user} <- Repo.insert!(%User{ name: user_params["userName"], email: user_params["email"], password_hash: encrPwd }) do
      conn
      |> put_status(:created)
      |> put_resp_header("location", user_path(conn, :show, user))
      |> render("show.json", user: user)
    end
  end

  def show(conn, %{"id" => id}) do
    user = Users.get_user!(id)
    render(conn, "show.json", user: user)
  end

  def update(conn, %{"id" => id, "user" => user_params}) do
    user = Users.get_user!(id)

    with {:ok, %User{} = user} <- Users.update_user(user, user_params) do
      render(conn, "show.json", user: user)
    end
  end

  def delete(conn, %{"id" => id}) do
    user = Users.get_user!(id)
    with {:ok, %User{}} <- Users.delete_user(user) do
      send_resp(conn, :no_content, "")
    end
  end
end
